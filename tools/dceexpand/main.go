package main

import (
	"flag"
	"log"
)

func main() {
	cfg := config{}
	flag.StringVar(&cfg.acceptedInputPath, "input", "", "Path to the accepted candidate JSON generated by tools/dcefilter.")
	flag.StringVar(&cfg.outputRoot, "output-root", "dataset/cases", "Output directory for materialized cases.")
	flag.StringVar(&cfg.runtimeRoot, "runtime-root", "dataset/runtime/csmith", "Vendored Csmith runtime directory.")
	flag.StringVar(&cfg.reportOutputPath, "report-out", "", "Optional path for the materialization report JSON.")
	flag.IntVar(&cfg.limit, "limit", 0, "Optional limit on the number of materialized cases.")
	flag.BoolVar(&cfg.excludeTimeouts, "exclude-timeouts", true, "Exclude cases whose source baseline times out within the default execution bound.")
	flag.BoolVar(&cfg.dropStale, "drop-stale", false, "Remove existing generated case directories under -output-root that were not materialized in this run.")
	flag.Parse()

	if err := run(cfg); err != nil {
		log.Fatal(err)
	}
}
